#include <iostream>
using namespace std;

int main()
{
	int x;
	int y;
	x=5;
	y=6;
	if (x==5)
		cout<<"The value of x is:"<<x<<endl;
	else
		cout<<"The value of x is not:"<<x<<endl;
	if (y=8)
		cout<<"The value of y is:"<<y<<endl;
	else
		cout<<"The value of y is not:"<<y<<endl;
	return 0;
}
/*
程序运行结果为：
The value of x is:5
The value of y is:8
*/